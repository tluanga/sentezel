class BalanceSheet {
  int creditors;
  int debtors;
  int capital;
  int netProfit;
  int netLoss;
  int cash;
  int bank;
  BalanceSheet({
    required this.creditors,
    required this.debtors,
    required this.capital,
    required this.netProfit,
    required this.netLoss,
    required this.cash,
    required this.bank,
  });
}
